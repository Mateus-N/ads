# Declaração da varável e atribuição do 2
# por ser o primeiro valor par entre 1 e 100
contador = 2

# Inicio da iteração que irá ocorrer
# enquanto o contador for menor que 100
while contador <= 100:

  # Imprimir na tela o contador
  print(contador)
  # Incrementar o contador a cada iteração
  # é adicionado 2 para que o contador sempre seja par
  contador += 2
